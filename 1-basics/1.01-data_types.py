# Data-Types are values in Python
# The fundamentals are:
    # int - for integers ie 1, 2, 3
    # float - for decimals ie 1.2
    # bool - truth or false values
    # str - strings
    # list - similar to JS arrays
    # tuple - marked by ()
    # set - organised list with no duplicates
    # dict - similar to JS objects {}

# CLASSES are custom data sets

# SPECIALISED DATA TYPES are not built into Python, but are packages/modules we use from libraries

# NONE is equivalent to 0 and represents no value