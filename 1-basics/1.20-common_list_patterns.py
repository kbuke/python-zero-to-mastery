# To calculate length of a list use the LEN() function
test_length_string = "Zahra"
print(f"Zahra has {len(test_length_string)} letters in her name.")

# To generate a list set in a range, use RANGE() function
print(list(range(1, 10)))

# To join an array of strings use the JOIN() function
new_sentence = " ".join(["Hi", "my", "name", "is", "Kaan"]) #leave space in string
print(new_sentence)

new_word = "".join(["T", "e", "s", "t"])
print(new_word)