# METHODS are similar but are owned by something and are marked by dots
quote = "to bE or not to be"
print(quote.upper()) #capitalises all letters
print(quote.capitalize()) #capitalise first letter
print(quote.lower()) #lowercases all letters
print(quote.find("bE")) #shows index of first occurance
print(quote.replace("bE", "me")) #replace 1st op with 2nd